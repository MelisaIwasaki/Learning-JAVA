//boolean expression

public class Main{
    public static void main(String[] args){
        int x = 10;
        int y = 9;
        
        System.out.println(x > y);   //return true, because 10 is higher than 9.
        System.out.println(x == 10); //return true, because the value of x is equal to 10.
        System.out.println(10 == 15);//return false, because 10 is not equal to 15.
        
        
    }
}
